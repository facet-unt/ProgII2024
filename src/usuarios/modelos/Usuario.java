package usuarios.modelos;
import java.util.Objects;
import java.util.ArrayList;
import pedidos.modelos.Pedido;

/**
 *
 * @author luis
 */
public abstract class Usuario {
    // Implementacion de las variables de instancia
    private String correo;
    private String clave;
    private String apellido;
    private String nombre;
    
    // Definicion del constructor
    public Usuario(String correo, String clave, String apellido, String nombre) {
        this.correo = correo;
        this.clave = clave;
        this.apellido = apellido;
        this.nombre = nombre;
    }

    // Definicion del metodo mostrar    
    public void mostrar() {
        System.out.println("Correo: " + correo);
        System.out.println("Clave: " + clave);
        System.out.println("Apellido: " + apellido);
        System.out.println("Nombre: " + nombre);
    }
    
    // Metodos equals y hashcode
    @Override
    public int hashCode() {
        int hash = 7;
        hash = 79 * hash + Objects.hashCode(this.correo);
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass().getSuperclass() != obj.getClass().getSuperclass()) {
            return false;
        }
        final Usuario other = (Usuario) obj;
        return Objects.equals(this.correo, other.correo);
    }
    
    // Definicion del metodo verPedidos
    
    public abstract ArrayList<Pedido> verPedidos();
    
    // Definicion de los metodos get/set de la clase para las 4 variables de instancia
    
    public String verCorreo() {
        return correo;
    }

    public void asignarCorreo(String correo) {
        this.correo = correo;
    }

    public String verClave() {
        return clave;
    }

    public void asignarClave(String clave) {
        this.clave = clave;
    }

    public String verApellido() {
        return apellido;
    }

    public void asignarApellido(String apellido) {
        this.apellido = apellido;
    }

    public String verNombre() {
        return nombre;
    }

    public void asignarNombre(String nombre) {
        this.nombre = nombre;
    }
    

}
